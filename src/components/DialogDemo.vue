<template>
  <div>DialogDemo</div>
  <h1>示例1</h1>
  <Button @click="toggle">toggle</Button>
  <Dialog v-model:visible="x" :cancel="f2" :close-on-click-overlay="false" :ok="f1"/>
</template>

<script lang="ts">
import Dialog from '../lib/Dialog.vue';
import Button from '../lib/Button.vue';
import {ref} from 'vue';

export default {
  name: 'DialogDemo',
  components: {Button, Dialog},
  setup() {
    const x = ref(false);
    const toggle = () => {
      x.value = !x.value;
    };
    const f1 = () => {
      console.log(1);
      return false;
    };
    const f2 = () => {
      console.log(2);
    };
    return {x, toggle, f1, f2};
  }
};

</script>

